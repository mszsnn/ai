
## 不含有特征缩放

import pandas as pd
import numpy as np
import copy
import matplotlib.pyplot as plt
# ==========================================
# 1. 模拟读取真实的表格数据 (DataFrame)
# ==========================================
# 在真实场景中，这行代码通常是：df = pd.read_csv('california_housing.csv')
# 这里我们用字典模拟从文件中读取了一个包含 5 条数据的表格
raw_data = {
    'Area(sqft)': [2104, 1416, 852, 1930, 1200, 1534, 1000, 2400],  # 面积
    'Bedrooms':   [5, 3, 2, 4, 3, 3, 2, 4],                # 卧室数
    'Floors':     [1, 2, 1, 2, 1, 2, 1, 2],   # 楼层数
    'Age(years)': [45, 40, 35, 15, 20, 30, 10, 5],     # 房龄
    'Price(1000$)':[460, 232, 178, 315, 200, 270, 190, 500]  # 真实的售价 (标签)
}

df = pd.DataFrame(raw_data)

print("--- 1. 真实拿到手的数据通常长这样 (带有表头，混在一起) ---")
print(df)
print("\n")


# ==========================================
# 2. 实战核心动作：将表格切分为 X 和 y
# ==========================================
# 提取特征 X：只要扔掉(drop) 'Price' 这一列，剩下的全是特征
X_df = df.drop('Price(1000$)', axis=1)

# 提取标签 y：只把 'Price' 这一列单独拎出来
y_df = df['Price(1000$)']
print("--- 2. 切分后的特征表格 X_df ---")
print(X_df)
print("\n--- 切分后的标签表格 y_df ---")
print(y_df)
print("\n")

# ==========================================
# 3. 降维打击：从表格剥离为 NumPy 矩阵
# ==========================================
# AI 引擎底层的 np.dot 是不认识 "Area"、"Bedrooms" 这些人类语言(表头)的。
# 我们需要用 .to_numpy() (或者老版本的 .values) 把外面的包装壳扔掉，只保留纯粹的数字矩阵。

X_train = X_df.to_numpy()
y_train = y_df.to_numpy()

print("--- 3. 最终喂给 AI 引擎的底层 NumPy 数据 ---")
print("X_train (形状:", X_train.shape, "):")
print(X_train)
print("\ny_train (形状:", y_train.shape, "):")
print(y_train)



# =====================================================================
# 2 核心引擎 损失函数
# y预测值 = w1*x1 + w2*x2 + ... + b
# 数学公式 j(w,b) = 1/2m * sum(y预测值- y实际值)^2
# =====================================================================

def compute_cost(X, y, w, b):
    m = X.shape[0] # 样本总数
    cost = 0.0  # 总损失
    for i in range(m):
    # ---------------------------------------------------
    # 【空间投影动作】：np.dot(X[i], w)
    # X[i] 是一维向量 [面积, 卧室, 楼层, 房龄]，比如 [2104, 5, 1, 45]
    # w 也是一维向量 [w1, w2, w3, w4]
    # 点积瞬间把 4 维空间的数据，压缩成了 1 维数轴上的一个点 (预测房价)
    # ---------------------------------------------------
      f_prev = np.dot(X[i], w) + b
      cost = cost + (f_prev - y[i]) ** 2
    return cost / (2 * m)


# =====================================================================
# 3. 核心引擎 2：梯度计算 (寻找下坡的最陡峭方向)
# 数学本质：利用微积分链式法则，求偏导数
# =====================================================================


def compute_gradient(X, y, w, b):
    m, n = X.shape  # m 房子数量 n 特征数量
    dj_dw = np.zeros(n,)  # 初始化 4个权重的梯度
    dj_db = 0.0

    for i in range(m):
        # 偏导数 其实就是  （预测值 - 实际值）* x
        err = np.dot(X[i], w) + b - y[i]

        # 有 n 个特征的偏导要求解
        # ====================================================================
        # 【公式对应】：因为有 n 个不同的权重 w_j，所以我们要把上面算出来的误差，
        # 分别乘上这套房子的 n 个特征值，并分配给对应的权重梯度。
        # ====================================================================
        for j in range(n):
            # 【核心数学动作】：这行代码实现了公式里的 (err) * x_j^(i) 并累加 (Σ)
            # dj_dw[j] = 之前的累加值 + 当前房子的误差 * 当前房子的第 j 个特征值
            # 这里的 X[i, j] 就完美对应了公式里的 x_j^(i)
            dj_dw[j] = dj_dw[j] + err * X[i, j]

        # b 的求导公式后面没有乘 x，所以直接把误差累加即可
        dj_db = dj_db + err

        # ====================================================================
        # 【公式对应】：循环结束，Σ 累加完成了！现在执行公式最前面的 (1 / m)
        # 把刚才累加的巨额总梯度，除以样本数量 m，求出"平均下坡方向"
        # ====================================================================

    dj_dw = dj_dw / m
    dj_db = dj_db / m

    return dj_dw, dj_db


# =====================================================================
# 4. 核心引擎 3：梯度下降主循环 (迈开腿往下走)
# =====================================================================

def gradient_descent(X, y, w_in, b_in, alpha, num_iterations):
    w = copy.deepcopy(w_in)
    b = b_in
    history = []

    for i in range(num_iterations):
        dw, db = compute_gradient(X, y, w, b)

        # ---------------------------------------------------
        # 2. 【核心数学动作】：更新位置 (向着梯度的反方向走)
        # w = 当前位置 - 步长 * 地形的陡峭程度
        # 为什么要减号？因为梯度指向的是上坡(误差变大)的方向，加个负号就是下坡！
        # ---------------------------------------------------

        w = w - alpha * dw
        b = b - alpha * db
        cost = compute_cost(X, y, w, b)

        history.append(cost)
# 每隔 10 步，向控制台汇报一下战况
        if i % 10 == 0:
            print(f"迭代 {i:4d} 次: Cost = {cost:10.2f}")


    return w,b,history


# =====================================================================
# 5. 指挥官控制台：设置超参数并点火发射！
# =====================================================================
n_features = X_train.shape[1] # 获取特征数量 (4)
w_init = np.zeros(n_features) # 把 4 个权重初始都设为 0
b_init = 0.

# ---------------------------------------------------
# 【不缩放数据的致命代价】：被迫使用极其微弱的学习率
# 因为"面积"达到了 2000 多，导致算出来的梯度极其庞大 (动不动就几万)。
# 如果 alpha 设置成 0.1，w = w - 0.1 * 几万，一次更新就会把权重甩到负几千，
# 导致下一次预测时误差变成几千万，瞬间爆炸 (Cost 变成无穷大 NaN)。
# 所以在不缩放时，只能小心翼翼地迈极其微小的步子 (0.0000005)。
# ---------------------------------------------------
iterations = 100
alpha = 5.0e-7

print("--- 开始训练 ---")
w_final, b_final, J_hist = gradient_descent(X_train, y_train, w_init, b_init, alpha, iterations)


# =====================================================================
# 6. 验收成果
# =====================================================================
print("\n--- 训练结束，查看真理之轴 ---")
# 你会发现：w[0] (面积权重) 变化显著，而 w[1], w[2], w[3] 几乎还是 0。
# 这就是特征尺度不统一导致的"跛脚"现象！
print(f"最终学到的权重 W: \n{w_final}")
print(f"最终学到的偏置 b: {b_final:.4f}\n")


# 绘制学习曲线：虽然 Cost 看起来在平滑下降，但其实离真正的最低点(谷底)还差了十万八千里。
plt.figure(figsize=(8, 4))
plt.plot(range(len(J_hist)), J_hist, c='b', linewidth=2)
plt.title("Learning Curve (NO Feature Scaling) - Very Slow Convergence")
plt.xlabel("Iterations")
plt.ylabel("Cost J")
plt.grid(True)
plt.show()